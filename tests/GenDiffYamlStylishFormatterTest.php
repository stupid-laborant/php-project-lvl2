{
    public function testTwoValidYml()
    {
        $expected = <<<DOC
{
    common: {
      + follow: false
        setting1: Value 1
      - setting2: 200
      - setting3: true
      + setting3: null
      + setting4: blah blah
      + setting5: {
            key5: value5
        }
        setting6: {
            doge: {
              - wow:
              + wow: so much
            }
            key: value
          + ops: vops
        }
    }
    group1: {
      - baz: bas
      + baz: bars
        foo: bar
      - nest: {
            key: value
        }
      + nest: str
    }
  - group2: {
        abc: 12345
        deep: {
            id: 45
        }
    }
  + group3: {
        deep: {
            id: {
                number: 45
            }
        }
        fee: 100500
    }
}
DOC;
        $firstYml = __DIR__ . "/mock/nonflat1.yml";
        $secondYml = __DIR__ . "/mock/nonflat2.yml";
        $this->assertEquals($expected, genDiff($firstYml, $secondYml));
    }
